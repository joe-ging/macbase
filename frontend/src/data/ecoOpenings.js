export const getOpeningName = (eco) => {
    if (!eco) return null;
    if (ECO_OPENINGS[eco]) return ECO_OPENINGS[eco];
    const prefix = eco.substring(0, 2) + '0';
    if (ECO_OPENINGS[prefix]) return ECO_OPENINGS[prefix];
    if (eco.startsWith('A')) return 'Flank Opening';
    if (eco.startsWith('B')) return 'Semi-Open Game';
    if (eco.startsWith('C')) return 'Open Game';
    if (eco.startsWith('D')) return 'Closed Game';
    if (eco.startsWith('E')) return 'Indian Defense';
    return null;
};

